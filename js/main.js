function animateCounters(){
  document.querySelectorAll('[data-count]').forEach(el=>{
    const target=parseFloat(el.dataset.count);
    const suffix=el.dataset.suffix||'';
    const isDecimal=el.dataset.decimal==='true';
    const dur=2000,start=performance.now();
    function step(now){
      const p=Math.min((now-start)/dur,1);
      const eased=1-Math.pow(1-p,3);
      el.textContent=(isDecimal?(eased*target).toFixed(1):Math.floor(eased*target))+suffix;
      if(p<1)requestAnimationFrame(step);
    }
    requestAnimationFrame(step);
  });
}
const trustObs=new IntersectionObserver(entries=>{
  entries.forEach(e=>{if(e.isIntersecting){animateCounters();trustObs.unobserve(e.target)}});
},{threshold:0.5});
document.querySelector('.hero-trust')&&trustObs.observe(document.querySelector('.hero-trust'));

