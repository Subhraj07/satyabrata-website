// HERO PARTICLE SYSTEM (Canvas)
// ═══════════════════════════════════════
const canvas=document.getElementById('heroParticles');
const ctx=canvas.getContext('2d');
let particles=[];
function resizeCanvas(){
  const hero=document.getElementById('hero');
  canvas.width=hero.offsetWidth;canvas.height=hero.offsetHeight;
}
resizeCanvas();window.addEventListener('resize',resizeCanvas);

class Particle{
  constructor(){this.reset()}
  reset(){
    this.x=Math.random()*canvas.width;this.y=Math.random()*canvas.height;
    this.size=Math.random()*2.5+0.5;
    this.speedY=-Math.random()*0.4-0.1;this.speedX=(Math.random()-0.5)*0.3;
    this.life=0;this.maxLife=Math.random()*300+200;
  }
  update(){
    this.x+=this.speedX;this.y+=this.speedY;this.life++;
    const r=this.life/this.maxLife;
    this.opacity=r<0.1?r*10:r>0.8?(1-r)*5:0.5;
    if(this.life>=this.maxLife||this.y<-10)this.reset();
  }
  draw(){
    ctx.beginPath();ctx.arc(this.x,this.y,this.size,0,Math.PI*2);
    ctx.fillStyle=`rgba(212,169,55,${this.opacity})`;ctx.fill();
    ctx.beginPath();ctx.arc(this.x,this.y,this.size*2,0,Math.PI*2);
    ctx.fillStyle=`rgba(232,115,26,${this.opacity*0.2})`;ctx.fill();
  }
}
const pCount=window.innerWidth<768?25:50;
for(let i=0;i<pCount;i++)particles.push(new Particle());
function animateParticles(){
  ctx.clearRect(0,0,canvas.width,canvas.height);
  particles.forEach(p=>{p.update();p.draw()});
  requestAnimationFrame(animateParticles);
}
animateParticles();

